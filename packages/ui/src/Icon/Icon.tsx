export type IconSize = 16 | 20 | 24 | 32 | 48 | 64

export interface IconProps extends SVGProps<SVGSVGElement> {
  /** Icon name from the available subset */
  name: IconName
  /** Icon size in pixels (16, 20, 24, 32, 48, 64) */
  size?: IconSize
}

const Icon: FC<IconProps> = ({ name, size = 24, className, ...rest }) => {
  let IconComponent: HeroIcon

  if (size <= 16) {
    IconComponent = iconMap16[name] ?? iconMap20[name]
  } else if (size <= 20) {
    IconComponent = iconMap20[name]
  } else {
    IconComponent = iconMap24[name]
  }

  return (
    <IconComponent className={className} width={size} height={size} {...rest} />
  )
}

export default Icon
